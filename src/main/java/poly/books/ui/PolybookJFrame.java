    public void openMenu() {
        menu.setSize(x, y);
        if (x == 0) {
            new Thread(new Runnable() {
                @Override
                public void run() {
                    try {
                        for (int i = 0; i <= 199; i++) {
                            menu.setSize(i, y);
                            Thread.sleep(1);
                        }
                    } catch (Exception e) {
                    }
                }
            }).start();
            x = 199;
        }
    }

    public void closeMenu() {
        menu.setSize(x, y);
        if (x == 199) {
            new Thread(new Runnable() {
                @Override
                public void run() {
                    try {
                        for (int i = 199; i >= 0; i--) {
                            menu.setSize(i, y);
                            Thread.sleep(2);
                        }
                    } catch (Exception e) {
                    }
                }
            }).start();
            x = 0;
        }
    }

