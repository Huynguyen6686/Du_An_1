    @Override
    public void setForm(Sach entity) {
        txtMaSach.setText(String.valueOf(entity.getMaSach()));
        txtTenSach.setText(entity.getTenSach());
        TacGia tacGia = TacGiaDAO.findByID(entity.getMaTacGia());
        txtTacGia.setText(tacGia != null ? tacGia.getTenTacGia() : "Không xác định");
        String tenLinhVuc = linhVucDAO.findByLinhVucID(entity.getMaSach()).stream()
                .map(LinhVuc::getTenLinhVuc).collect(Collectors.joining(", "));
        txtLV.setText(tenLinhVuc.isEmpty() ? "" : tenLinhVuc);
        String tenLoaiSach = loaiSachDAO.findBySachID(entity.getMaSach()).stream()
                .map(LoaiSach::getTenLoaiSach).collect(Collectors.joining(", "));
        txtTL.setText(tenLoaiSach.isEmpty() ? "" : tenLoaiSach);
        NhaXuatBan nxb = nhaXuatBanDAO.findByID(entity.getMaNXB());
        txtNhaXuatBan.setText(nxb != null ? nxb.getTenNXB() : "Không xác định");
        txtNamSX.setText(entity.getNamXuatBan() != null ? entity.getNamXuatBan().toString() : "");
        txtGiaBan.setText(String.valueOf(entity.getGiaBan()));
        txtLanTB.setText(String.valueOf(entity.getLanTaiBan()));
        txtISNB.setText(entity.getISBN());
        txtTap.setText(entity.getTap() != null ? String.valueOf(entity.getTap()) : "");
        NgonNgu ngonNgu = ngonNguDAO.findByID(entity.getMaNgonNgu());
        txtNgonNgu.setText(ngonNgu != null ? ngonNgu.getTenNgonNgu() : "Không xác định");
        lbAnh.setIcon(null);
        lbAnh.setText("");
        if (entity.getHinhAnh() != null && !entity.getHinhAnh().isEmpty()) {
            lbAnh.setToolTipText(entity.getHinhAnh());
            java.net.URL imageUrl = getClass().getResource("/imgSach/" + entity.getHinhAnh());
            if (imageUrl != null) {
                ImageIcon icon = new ImageIcon(imageUrl);
                Image img = icon.getImage().getScaledInstance(lbAnh.getWidth(), lbAnh.getHeight(), Image.SCALE_SMOOTH);
                lbAnh.setIcon(new ImageIcon(img));
            }
        }
    }

