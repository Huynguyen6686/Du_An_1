    @Override
    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tbDanhSachSP.getModel();
        model.setRowCount(0);
        model.setColumnIdentifiers(new String[]{
            "Mã Sách", "Tên Sách", "Tác Giả", "Lĩnh Vực", "Thể Loại", "Nhà XB",
            "Năm XB", "Giá Bán", "Lần Tái Bản", "ISBN", "Tập", "Ngôn Ngữ", "Số Lượng", "Hình Ảnh"
        });
        for (Sach sach : sachDAO.getAllDanhSachSP()) {
            TacGia tacGia = TacGiaDAO.findByID(sach.getMaTacGia());
            NhaXuatBan nxb = nhaXuatBanDAO.findByID(sach.getMaNXB());
            NgonNgu ngonNgu = ngonNguDAO.findByID(sach.getMaNgonNgu());
            String tenLinhVuc = linhVucDAO.findByLinhVucID(sach.getMaSach()).stream()
                    .map(LinhVuc::getTenLinhVuc).collect(Collectors.joining(", "));
            String tenLoaiSach = loaiSachDAO.findBySachID(sach.getMaSach()).stream()
                    .map(LoaiSach::getTenLoaiSach).collect(Collectors.joining(", "));

            Object[] row = {
                sach.getMaSach(),
                sach.getTenSach(),
                tacGia != null ? tacGia.getTenTacGia() : "Không xác định",
                tenLinhVuc.isEmpty() ? "" : tenLinhVuc,
                tenLoaiSach.isEmpty() ? "" : tenLoaiSach,
                nxb != null ? nxb.getTenNXB() : "Không xác định",
                sach.getNamXuatBan(),
                sach.getGiaBan(),
                sach.getLanTaiBan(),
                sach.getISBN(),
                sach.getTap(),
                ngonNgu != null ? ngonNgu.getTenNgonNgu() : "Không xác định",
                sach.getSoLuong(),
                sach.getHinhAnh() != null ? sach.getHinhAnh() : ""
            };
            model.addRow(row);
        }
    }

