    @Override
    public void setForm(NguoiDungSD ndsd) {
        txtTendangnhap.setText(String.valueOf(ndsd.getTenDangNhap()));
        txtMatKhau.setText(String.valueOf(ndsd.getMatKhau()));
        txtHovaten.setText(String.valueOf(ndsd.getHoTen()));
        
        if (ndsd.isTrangThai()) {
            rdoConLam.setSelected(true);
        } else {
            rdoNghilam.setSelected(true);
        }

        if (ndsd.isQuanLy()) {
            rdoQuanLy.setSelected(true);
        } else {
            rdoNhanVien.setSelected(true);
        }
        
        lblAnh.setIcon(null);
            lblAnh.setText("");
            if (ndsd.getHinhAnh() != null && !ndsd.getHinhAnh().isEmpty()) {
                lblAnh.setToolTipText(ndsd.getHinhAnh());
                java.net.URL imageUrl = getClass().getResource("/New folder (2)/" + ndsd.getHinhAnh());
                if (imageUrl != null) {
                    ImageIcon icon = new ImageIcon(imageUrl);
                    Image img = icon.getImage().getScaledInstance(lblAnh.getWidth(), lblAnh.getHeight(), Image.SCALE_SMOOTH);
                    lblAnh.setIcon(new ImageIcon(img));
                }
            }
    }

    @Override
    public NguoiDungSD getForm() {
        NguoiDungSD nd = new NguoiDungSD();
        nd.setTenDangNhap(txtTendangnhap.getText().trim());
        nd.setMatKhau(new String(txtMatKhau.getText()).trim());
        nd.setHoTen(txtHovaten.getText().trim());

        nd.setTrangThai(rdoConLam.isSelected());
        nd.setQuanLy(rdoQuanLy.isSelected());

        nd.setHinhAnh(lblAnh.getToolTipText()); // giả sử bạn dùng toolTipText để lưu tên file ảnh
        return nd;
    }

    @Override
    public void fillToTable() {
        DefaultTableModel defaultTableModel=(DefaultTableModel) tblTaiKhoan.getModel();
        defaultTableModel.setRowCount(0);
        listndsd=ndsddao.findAll();
        for(NguoiDungSD ndsd:listndsd){
           defaultTableModel.addRow(new Object[]{
            ndsd.getTenDangNhap(),
            ndsd.getMatKhau(),
            ndsd.getHoTen(),
            ndsd.isTrangThai() ? "Còn làm" : "Nghỉ làm",
            ndsd.isQuanLy() ? "Quản lý" : "Nhân viên",
            ndsd.getHinhAnh()
        }); 
        }
                
    }

    @Override
    public void create() {
        NguoiDungSD nd = getForm();
    if (ndsddao.findById(nd.getTenDangNhap()) != null) {
        MsgBox.alert(this, "Tên đăng nhập đã tồn tại!");
        return;
    }
    try {
        ndsddao.craete(nd);
        fillToTable();
        clear();
        MsgBox.alert(this, "Thêm mới thành công!");
    } catch (Exception e) {
        e.printStackTrace();
        MsgBox.alert(this, "Thêm mới thất bại!");
    }
    }

    @Override
    public void update() {
        NguoiDungSD nd = getForm();
        try {
            ndsddao.update(nd);
            fillToTable();
            MsgBox.alert(this, "Cập nhật thành công!");
        } catch (Exception e) {
        e.printStackTrace();
        MsgBox.alert(this, "Cập nhật thất bại!");
    }
    }

    @Override
    public void delete() {
        String TenDangNhap = txtTendangnhap.getText();
        if (TenDangNhap.isEmpty()) {
            MsgBox.alert(this, "Vui lòng chọn tài khoản cần xóa!");
            return;
            }

        if (MsgBox.confirm(this, "Bạn có chắc chắn muốn xóa tài khoản này?")) {
            try {
                ndsddao.detele(TenDangNhap);
                fillToTable();
                clear();
                MsgBox.alert(this, "Xóa thành công!");
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "Xóa thất bại!");
        }
        }
    }

